package Loops;

public class ForLoop {
	public static void main(String[] args) {
		  
		   // create an array
		   int[] age = {4, 23, 4, 12, 4, 5};

		   // loop through the array
		   // using for loop
		   System.out.println("Using for Loop:");
		   for(int i = 0; i < age.length; i++) {
		     System.out.println(age[i]);
		   }
	}

}
