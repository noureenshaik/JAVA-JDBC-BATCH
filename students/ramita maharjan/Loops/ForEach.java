package Loops;

public class ForEach {
	public static void main(String[] args) {
		  
		   // create an array
		   int[] age = {4, 23, 4, 12, 4, 5};

		   // loop through the array
		   // using for loop
		   System.out.println("Using for each Loop:");
		   for(int i: age) {
		     System.out.println(i);
		   }
	}
}
