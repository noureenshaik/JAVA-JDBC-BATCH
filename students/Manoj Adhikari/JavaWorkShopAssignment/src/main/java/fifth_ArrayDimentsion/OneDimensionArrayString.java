package fifth_ArrayDimentsion;

import java.util.Scanner;

public class OneDimensionArrayString {

    public static void main(String[] args) {
        // Declaring and initializing the string array
        String [] strArray={" Alive ", "Be patient", "Be yourself"};
        System.out.println("The lenght of string array is : "+ strArray.length);

        System.out.println("Printing one dimension array : ");
        for(int i=0;i<strArray.length;i++){
            System.out.print( strArray[i]+ " \t");


        }



    }




}
